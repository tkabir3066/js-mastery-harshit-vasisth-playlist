//---rules for naming variables--//

// you cannot start with number
// example :-
// 1value (invalid)
// value1 (valid)


//you can use only underscore_ or dollar symbol
// first_name (valid)
// _firstName (valid)


// first$name (valid)
// $firstName (valid)


// you cannot use spaces
// first name (invalid)


// convention
// start with small letter and camelCase